def sqrt_a(ary)
  n = ary.size - 1
  a = [1, ary[1] / 2]
  i = 1
  while i < n
    a << (ary[i + 1] - (1..i).inject(0){|s, j| s + a[j] * a[-j]}) / 2
    i += 1
  end
  a
end

def I(ary, n)
  a = [1]
  i = 0
  while i < n
    a << -(0..i).inject(0){|s, j| s + ary[1 + i - j] * a[j]}
    i += 1
  end
  a
end

