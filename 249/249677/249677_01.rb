# m次以下を取り出す
def mul(f_ary, b_ary, m)
  s1, s2 = f_ary.size, b_ary.size
  ary = Array.new(s1 + s2 - 1, 0)
  (0..s1 - 1).each{|i|
    (0..s2 - 1).each{|j|
      ary[i + j] += f_ary[i] * b_ary[j]
    }
  }
  ary[0..m]
end

def A(n)
  c_ary = [1]
  ary = [[1]]
  (1..n).each{|i|
    c_ary = mul(c_ary, [1, i * i * i], i)
    ary << c_ary
  }
  ary.flatten
end

