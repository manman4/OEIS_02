def bernoulli(n)
  ary = []
  a = []
  (0..n).each{|i|
    a << 1r / (i + 1)
    i.downto(1){|j| a[j - 1] = j * (a[j - 1] - a[j])}
    ary << a[0] # Bn = a[0]
  }
  ary
end

def A(n)
  a = bernoulli(n)
  ary = []
  m = 0
  (0..n).each{|i|
    j = a[i].numerator.abs
    if j > m
      ary << j
      m = j
    end
  }
  ary
end

