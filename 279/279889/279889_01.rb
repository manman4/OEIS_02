require 'prime'

def power(a, n)
  return 1 if n == 0
  k = power(a, n >> 1)
  k *= k
  return k if n & 1 == 0
  return k * a
end

def sigma(x, i)
  sum = 1
  pq = i.prime_division
  pq.each{|a, n| sum *= (power(a, (n + 1) * x) - 1) / (power(a, x) - 1)}
  sum
end

def A(k, n)
  (1..n).map{|i| (1..i - 1).inject(0){|s, j| s + sigma(k, j) * sigma(k, i - j)}}
end

n = 25
ary = A(5, n)
